#include "../include/minishell.h"

int	ft_isalnum(unsigned char c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else if (c >= '0' && c <= '9')
		return (2);
	else
		return (0);
}
